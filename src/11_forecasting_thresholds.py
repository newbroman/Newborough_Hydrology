# SECTION 1: MECHANISTIC STATE-SPACE EQUATIONS
# ====================================================================================
def run_state_space(df: pd.DataFrame) -> dict:
    """
    Fit the state-space model for each qualifying cluster column.

    Model (no intercept — physical mass-balance constraint):
        Δhₜ = β₁·Pₜ − β₂·PETₜ − β₃·hₜ₋₁

    PET and h_prev are sign-inverted before fitting so that all three
    reported β coefficients emerge as positive values in the equation.

    Returns:
        results_dict: {cluster_name: {beta1, beta2, beta3, r2, n}}
    """
    print(sep("SECTION 1: MECHANISTIC STATE-SPACE EQUATIONS  (Δh = β₁P − β₂PET − β₃h_prev)"))
    print("  OLS without intercept — enforces physical mass balance (zero forcing → zero change)\n")

    # Macro-block/cluster mapping for print labels
    cluster_labels = {
        'C1': 'C1: Eastern Block Lake',
        'C2': 'C2: Eastern Block Mature Dune',
        'C3': 'C3: Western Block Mature Dune',
        'C4': 'C4: Forest',
        'C5': 'C5: Coastal',
        'C6': 'C6: Lake'
    }
    cluster_cols = sorted(
        c for c in df.columns
        if re.fullmatch(r'C\d+', c) and c not in EXCLUDED_CLUSTERS
    )
    results_dict = {}

    if not cluster_cols:
        print("  [WARNING] No qualifying cluster columns found. Check EXCLUDED_CLUSTERS setting.")
        return results_dict

    for col in cluster_cols:
        # Build a working frame with only the columns we need
        work = df[[col, 'P_mm', 'PET_mm']].copy()
        work['h_prev']  = work[col].shift(1)
        work['Delta_h'] = work[col] - work['h_prev']
        work = work.dropna(subset=['Delta_h', 'P_mm', 'PET_mm', 'h_prev'])

        if len(work) < 30:
            print(f"  {cluster_labels.get(col, col)}: Insufficient data ({len(work)} rows) — skipped.")
            continue

        # Invert PET and h_prev so β₂ and β₃ are reported as positive quantities
        X = pd.DataFrame({
            'P_mm':   work['P_mm'],
            'PET_mm': -work['PET_mm'],   # sign inversion
            'h_prev': -work['h_prev'],   # sign inversion
        }, index=work.index)

        model = sm.OLS(work['Delta_h'], X).fit()   # no intercept (through origin)
        b1, b2, b3 = model.params['P_mm'], model.params['PET_mm'], model.params['h_prev']
        pb1 = model.pvalues['P_mm']
        pb2 = model.pvalues['PET_mm']
        pb3 = model.pvalues['h_prev']

        print(
            f"  {cluster_labels.get(col, col)}:  Δh = ({b1:.4f}·P) − ({b2:.4f}·PET) − ({b3:.4f}·h_prev)"
            f"   |  R² = {model.rsquared:.3f}   n = {len(work)}"
        )
        print(
            f"        p-values:  P = {pb1:.4f}   PET = {pb2:.4f}   h_prev = {pb3:.4f}"
        )

        results_dict[col] = {
            'beta1': b1,
            'beta2': b2,
            'beta3': b3,
            'r2':    model.rsquared,
            'n':     len(work),
        }

    print(f"\n  Excluded from mechanistic model: {', '.join(sorted(EXCLUDED_CLUSTERS))}"
          f"  (boundary-condition clusters — see script header)")

    return results_dict


