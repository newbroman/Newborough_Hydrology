r"""
====================================================================================
GROUNDWATER FORECASTING MODELS (forecasting_models.py)
====================================================================================
Purpose:
  Takes the monthly cluster-averaged output from LCSC3.py and performs three
  complementary analyses for manuscript publication:

    SECTION 1 — Mechanistic State-Space Equations
        Fits the multivariate state-space model to each block (Eastern, Western, Forest):
                Δhₜ = β₁Pₜ − β₂PETₜ − β₃hₜ₋₁
        Regression is performed without an intercept (OLS through the origin) to
        enforce physical mass balance: when all forcing terms are zero, the change
        in water-table elevation must also be zero.

    SECTION 2 — Peak Flood Transfer Functions
        Constructs empirical transfer functions for the Eastern, Western, and Forest
        blocks (when available) that predict maximum winter water level from:
        h_peak = β₁·P_winter + β₂·h_min + c
    where h_min is the preceding summer drought minimum (April–September) and
    P_winter is cumulative precipitation over October–March.
    Data are indexed by Hydrological Year (starting 1 October) to keep each
    recharge cycle intact.

  SECTION 3 — Critical Rainfall Thresholds (Management Tool)
    Algebraically inverts the Section 1 equations (setting winter PET = 0)
    to derive the critical rainfall needed to raise the water table to flood
    surface from any given antecedent depth:
        P_flood = (h_gap + β₃·h_prev) / β₁
    Outputs ready-to-use LibreOffice and LaTeX expressions for each cluster.

    SECTION 4 — Summer Drought Transfer Functions
        Fits empirical OLS models for each block predicting the summer
    drought minimum (h_min_summer) from antecedent winter storage and
    cumulative summer rain:
        h_min_summer = β₁·P_summer + β₂·h_max_winter + c
    Complements Section 2 by characterising the drying leg of the annual
    water-table cycle.

Dependencies:
    - 03_regional_averages.csv  (generated by 03_state_space_model.py)
  - pandas, statsmodels

Outputs:
  - outputs/11_forecasting_thresholds/11_forecast_01_results.txt

Excluded clusters:
    C5 (coastal) and C6 (lake-buffered) are excluded from Section 1 mechanistic state-space equations
    because its boundary-condition behaviour violates free-draining assumptions.
    All block transfer-function sections (2 and 4) and mechanistic equations (1, 3)
    use the block time-series exported by LCSC03 (Eastern, Western, Forest) when present.
====================================================================================
"""

