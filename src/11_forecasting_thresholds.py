# SECTION 4: SUMMER DROUGHT TRANSFER FUNCTIONS
# ====================================================================================
def run_summer_drought_forecasting(df: pd.DataFrame) -> None:
    """
    Fit empirical summer drought transfer functions for available blocks
    (Western, Eastern, and Forest where provided).

    For each Hydrological Year (Oct 1 – Sep 30):
        h_max_winter = maximum water level over Oct–Mar (antecedent winter storage)
        P_summer     = cumulative precipitation over Apr–Sep
        h_min_summer = minimum water level over Apr–Sep (target drought variable)

    Regression (OLS with intercept):
        h_min_summer = β₁·P_summer + β₂·h_max_winter + c
    """
    print(sep("SECTION 4: SUMMER DROUGHT TRANSFER FUNCTIONS  (h_min = β₁·P_summer + β₂·h_max_winter + c)"))
    print("  OLS with intercept | Hydrological year: Oct 1 – Sep 30")
    print("  Predicting summer minimum from antecedent winter peak and summer rain.\n")

    df = df.copy()
    df.index = pd.to_datetime(df.index)
    df = df.sort_index()
    df['Hydro_Year'] = df.index.year + (df.index.month >= 10).astype(int)

    blocks = [block for block in ['Western_Block', 'Eastern_Block', 'Forest'] if block in df.columns]
    table7_rows = []
    for block in blocks:
        if block not in df.columns:
            continue

        yearly_data = []

        for year, subset in df.groupby('Hydro_Year'):
            winter = subset[subset.index.month.isin(WINTER_MONTHS)][[block]].dropna()
            summer = subset[subset.index.month.isin(SUMMER_MONTHS)][[block, 'P_mm']].dropna()

            if len(summer) >= MIN_VALID_MONTHS and len(winter) >= MIN_VALID_MONTHS:
                yearly_data.append({
                    'h_min_summer': summer[block].min(),
                    'h_max_winter': winter[block].max(),
                    'P_summer':     summer['P_mm'].sum(),
                })

        hydro_df = pd.DataFrame(yearly_data).dropna()

        if len(hydro_df) < 5:
            print(f"  {block}: Insufficient hydrological years ({len(hydro_df)}) — skipped.")
            continue

        X     = sm.add_constant(hydro_df[['P_summer', 'h_max_winter']])
        model = sm.OLS(hydro_df['h_min_summer'], X).fit()

        c   = model.params['const']
        b1  = model.params['P_summer']
        b2  = model.params['h_max_winter']
        pc  = model.pvalues['const']
        pb1 = model.pvalues['P_summer']
        pb2 = model.pvalues['h_max_winter']

        print(f"  {block}:")
        print(f"    h_min_summer = ({b1:.5f}·P_summer) + ({b2:.3f}·h_max_winter) {'+' if c >= 0 else '-'} {abs(c):.3f}")
        print(f"    R² = {model.rsquared:.3f}   |   n = {len(hydro_df)} hydrological years")
        print(f"    p-values:  P_summer = {pb1:.4f}   h_max_winter = {pb2:.4f}   intercept = {pc:.4f}")
        print()

        table7_rows.append({
            'Block': block,
            'beta_1_P_summer': float(b1),
            'beta_2_h_max_winter': float(b2),
            'intercept': float(c),
            'R2': float(model.rsquared),
            'n_hydrological_years': int(len(hydro_df)),
            'p_value_P_summer': float(pb1),
            'p_value_h_max_winter': float(pb2),
            'p_value_intercept': float(pc),
            'equation': f"h_min_summer = ({b1:.5f}*P_summer) + ({b2:.3f}*h_max_winter) {'+' if c >= 0 else '-'} {abs(c):.3f}",
        })

    pd.DataFrame(table7_rows).to_csv(OUT_11_TABLE7_SUMMER, index=False)

