class _Tee:
    """
    Context manager that writes every print() call to both the terminal
    (original stdout) and a plain-text file simultaneously.

    Usage::
        with _Tee(path):
            run_models()   # all output goes to terminal AND file
    """
    def __init__(self, filepath: Path):
        self._filepath: Path              = filepath
        self._orig_out                    = sys.stdout
        self._fh: Optional[IOBase]        = None

    def write(self, data: str) -> None:
        self._orig_out.write(data)
        if self._fh:
            self._fh.write(data)

    def flush(self) -> None:
        self._orig_out.flush()
        if self._fh:
            self._fh.flush()

    def __enter__(self):
        self._fh = open(self._filepath, 'w', encoding='utf-8')
        sys.stdout = self
        return self

    def __exit__(self, *_):
        sys.stdout = self._orig_out
        self._fh.close()
        print(f"\n[OUTPUT] Results transcript saved \u2192 {self._filepath}")


