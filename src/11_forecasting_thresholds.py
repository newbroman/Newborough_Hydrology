# SECTION 2: PEAK FLOOD TRANSFER FUNCTIONS
# ====================================================================================
def run_peak_forecasting(df: pd.DataFrame) -> None:
    """
    Fit empirical peak-flood transfer functions for available blocks
    (Western, Eastern, and Forest where provided).

    For each Hydrological Year (Oct 1 – Sep 30):
        h_min    = minimum water level over Apr–Sep  (summer drought memory)
        P_winter = cumulative precipitation over Oct–Mar
        h_peak   = maximum water level over Oct–Mar  (target variable)

    Regression (OLS with intercept):
        h_peak = β₁·P_winter + β₂·h_min + c

    Note on block handling:
        Uses exported block columns from LCSC03 directly. Western_Block and
        Eastern_Block are expected; Forest and Coastal are included when available.
    """
    print(sep("SECTION 2: PEAK FLOOD TRANSFER FUNCTIONS  (h_peak = β₁·P_winter + β₂·h_min + c)"))
    print("  OLS with intercept | Hydrological year: Oct 1 – Sep 30")
    print("  Macro-blocks: Eastern Block (C1 & C2), Western Block (C3), Forest (C4).\n")
    print("  Note: C6 is a boundary outlier (lake/tidal) and is excluded from standard unconfined assumptions.\n")

    # Defensive: ensure index is DatetimeIndex and sorted
    df = df.copy()
    df.index = pd.to_datetime(df.index)
    df = df.sort_index()

    # Tag each row with its Hydrological Year
    # Oct/Nov/Dec → belongs to the year starting that October (year + 1 label)
    # Jan–Sep     → belongs to the year that started the previous October
    df['Hydro_Year'] = df.index.year + (df.index.month >= 10).astype(int)

    # Use exported macro-block columns when present.
    blocks = [block for block in ['Western_Block', 'Eastern_Block', 'Forest'] if block in df.columns]
    table6_rows = []
    for block in blocks:
        if block not in df.columns:
            print(f"  {block}: column not found in data — skipped.")
            continue

        yearly_data = []

        for year, subset in df.groupby('Hydro_Year'):
            # Drop NaNs for this block only to avoid cross-column contamination
            summer = subset[subset.index.month.isin(SUMMER_MONTHS)][[block]].dropna()
            winter = subset[subset.index.month.isin(WINTER_MONTHS)][[block, 'P_mm']].dropna()

            # Require MIN_VALID_MONTHS per season for a reliable min/max
            if len(summer) >= MIN_VALID_MONTHS and len(winter) >= MIN_VALID_MONTHS:
                yearly_data.append({
                    'h_peak':   winter[block].max(),
                    'h_min':    summer[block].min(),
                    'P_winter': winter['P_mm'].sum(),
                })

        hydro_df = pd.DataFrame(yearly_data).dropna()

        if len(hydro_df) < 5:
            print(f"  {block}: Insufficient hydrological years ({len(hydro_df)}) — skipped.")
            continue

        X     = sm.add_constant(hydro_df[['P_winter', 'h_min']])
        model = sm.OLS(hydro_df['h_peak'], X).fit()

        c   = model.params['const']
        b1  = model.params['P_winter']
        b2  = model.params['h_min']
        pc  = model.pvalues['const']
        pb1 = model.pvalues['P_winter']
        pb2 = model.pvalues['h_min']

        print(f"  {block}:")
        print(f"    h_peak = ({b1:.5f}·P_winter) + ({b2:.3f}·h_min) {'+' if c >= 0 else '-'} {abs(c):.3f}")
        print(f"    R² = {model.rsquared:.3f}   |   n = {len(hydro_df)} hydrological years")
        print(f"    p-values:  P_winter = {pb1:.4f}   h_min = {pb2:.4f}   intercept = {pc:.4f}")
        print()

        table6_rows.append({
            'Block': block,
            'beta_1_P_winter': float(b1),
            'beta_2_h_min': float(b2),
            'intercept': float(c),
            'R2': float(model.rsquared),
            'n_hydrological_years': int(len(hydro_df)),
            'p_value_P_winter': float(pb1),
            'p_value_h_min': float(pb2),
            'p_value_intercept': float(pc),
            'equation': f"h_peak = ({b1:.5f}*P_winter) + ({b2:.3f}*h_min) {'+' if c >= 0 else '-'} {abs(c):.3f}",
        })

    pd.DataFrame(table6_rows).to_csv(OUT_11_TABLE6_WINTER, index=False)


