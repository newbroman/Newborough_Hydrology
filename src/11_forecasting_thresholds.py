r"""
====================================================================================
GROUNDWATER FORECASTING MODELS (11_forecasting_thresholds.py)
====================================================================================
Purpose:
  Takes the monthly cluster-averaged output from 03_state_space_model.py and
  performs four complementary analyses supporting Section 3.6 of Hollingham
  (2026):

    SECTION 1 — Mechanistic State-Space Equations
        Loads cluster-centroid lag-1 SSM coefficients from Script 03's
        mechanistic coefficients table (03_03_cluster_mechanistic_coefficients.csv)
        and converts from m/m to m/mm for downstream use. Reports coefficients
        and p-values for the transcript. Previously fitted its own lag-0 OLS
        on cluster-average hydrographs; superseded by Script 03's lag-1 fits.

    SECTION 2 — Peak Flood Transfer Functions
        Constructs empirical transfer functions for the Eastern, Western, and
        Forest blocks that predict maximum winter water level from:
            h_peak = β₁·P_winter + β₂·h_min + c
        where h_min is the preceding summer drought minimum (April-September)
        and P_winter is cumulative precipitation over October-March.
        Data are indexed by Hydrological Year (starting 1 October) to keep
        each recharge cycle intact.

    SECTION 3 — Critical Rainfall Thresholds (Management Tool)  [REVISED 2026-04]
        Iterates the Section 1 monthly recurrence forward from the September
        summer minimum to each cluster's historical peak-of-record month, with
        rainfall scaled by a multiplier λ and PET held at RAF Valley monthly
        climatology. Solves the resulting closed form for λ:
            λ = (h_target - h_0·αⁿ + β₂·S_E) / (β₁·S_P)
            P_flood = λ · Σ P̄ᵢ
        where α = 1 - β₃, S_P and S_E are drainage-weighted climatology sums,
        and n is the cluster-specific horizon length.

        Supersedes the earlier single-step formulation
            P_flood = (h_gap + β₃·h_prev) / β₁
        which neglected winter PET (~30% of winter atmospheric demand at RAF
        Valley) and implicitly treated the six-month recharge season as a
        single SSM timestep. The old formulation is retained in the output
        CSV under column "P_flood_old_single_step_mm" for continuity.

        See revised Section 3.6.3 of Hollingham (2026) for full derivation.

    SECTION 4 — Summer Drought Transfer Functions
        Fits empirical OLS models for each block predicting the summer drought
        minimum (h_min_summer) from antecedent winter storage and cumulative
        summer rainfall:
            h_min_summer = β₁·P_summer + β₂·h_max_winter + c
        Complements Section 2 by characterising the drying leg of the annual
        water-table cycle.

Dependencies:
    - 03_regional_averages.csv  (generated by 03_state_space_model.py)
    - pandas, statsmodels

Outputs:
    - outputs/11_forecasting_thresholds/11_forecast_01_results.txt
    - Table 6: winter transfer functions  (11_forecast_winter_transfer_functions.csv)
    - Table 7: summer transfer functions  (11_forecast_summer_transfer_functions.csv)
    - Table 8: P_flood threshold equations (11_forecast_pflood_threshold_equations.csv)

Cluster scope (k=5 partition):
    All five clusters participate in mechanistic and block-transfer sections.
    The original old-k=6 EXCLUDED_CLUSTERS set (C5 tidal, C6 lake-buffered)
    targeted small-n / unreliable groups; under the current partition those
    were cleaned out at the partition level, so EXCLUDED_CLUSTERS is empty.
    Block columns are produced by 03_state_space_model.py via BLOCK_MAP:
    Lake_Edge (C1), Eastern_Block (C2), Western_Block (C3), Forest (C4),
    Coastal_Forest (C5).
====================================================================================
"""

