def _load_cluster_peak_months() -> dict[str, int]:
    """
    Load CLUSTER_PEAK_MONTH from INT_CLUSTER_PEAK_MONTHS (written by
    Script 03's export_cluster_peak_months). The file is the single source of
    truth: it is derived from the long-term monthly means of the cluster-
    centroid hydrograph, and is regenerated each time Script 03 runs.

    Failing loudly if the file is missing is intentional — silently falling
    back to stale defaults would mean using forecasting horizons keyed to
    the wrong partition.
    """
    if not INT_CLUSTER_PEAK_MONTHS.exists():
        raise FileNotFoundError(
            f"Cluster peak-months file not found:\n  {INT_CLUSTER_PEAK_MONTHS}\n"
            f"Run script 03 (state-space model) first — it produces this file "
            f"as part of its regional-averages exports."
        )
    df = pd.read_csv(INT_CLUSTER_PEAK_MONTHS)
    if not {"cluster_id", "peak_month"}.issubset(df.columns):
        raise RuntimeError(
            f"{INT_CLUSTER_PEAK_MONTHS.name} is missing expected columns "
            f"'cluster_id' and 'peak_month'. Re-run script 03 to regenerate."
        )
    return {f"C{int(row['cluster_id'])}": int(row["peak_month"])
            for _, row in df.iterrows()}


