# SECTION 3: CRITICAL RAINFALL THRESHOLD EQUATIONS
# ====================================================================================
def run_critical_flood_thresholds(results_dict: dict) -> None:
    """
    Inverts the Section 1 state-space equations to solve for P_flood.

    Derivation (winter PET ≈ 0 — standard hydrological simplification for
    peak saturation events when evaporative demand is negligible):

        Δh = β₁·P − β₃·h_prev         (PET term dropped)
        Setting Δh = h_gap  (deficit to flood surface from current h_prev):
        P_flood = (h_gap + β₃·h_prev) / β₁

    Because the coefficients are taken directly from results_dict, the
    management equations update automatically if the source data changes.
    """
    print(sep("SECTION 3: CRITICAL RAINFALL THRESHOLD EQUATIONS  (P_flood)"))
    print("  Algebraically inverted from Section 1 state-space mechanics.")
    print("  Assumes winter PET = 0 (defensible for peak-saturation events).\n")

    # Only fitted, non-excluded clusters (ordered by cluster ID)
    target_clusters = sorted(
        (cluster for cluster in results_dict if cluster not in EXCLUDED_CLUSTERS),
        key=lambda name: (0, int(name[1:])) if name.startswith('C') and name[1:].isdigit() else (1, name),
    )

    cluster_labels = {
        'C1': 'C1: Eastern Block Lake',
        'C2': 'C2: Eastern Block Mature Dune',
        'C3': 'C3: Western Block Mature Dune',
        'C4': 'C4: Forest',
        'C5': 'C5: Coastal',
        'C6': 'C6: Lake'
    }
    table8_rows = []
    for cluster in target_clusters:
        if cluster not in results_dict:
            print(f"  {cluster_labels.get(cluster, cluster)}: no fitted parameters available — skipped.")
            continue

        b1 = results_dict[cluster]['beta1']
        b3 = results_dict[cluster]['beta3']

        # Special label for C4
        if cluster == 'C4':
            print(f"  C4: Forest Management Tool:")
        else:
            print(f"  {cluster_labels.get(cluster, cluster)} Management Tool:")
        print(f"    LibreOffice: =(h_gap + ({b3:.4f} * h_prev)) / {b1:.4f}")
        print(f"    LaTeX:       P_{{flood}} = \\frac{{h_{{gap}} + ({b3:.4f} \\cdot h_{{prev}})}}{{{b1:.4f}}}")
        print()

        table8_rows.append({
            'Cluster': cluster,
            'Label': cluster_labels.get(cluster, cluster),
            'beta_1': float(b1),
            'beta_3': float(b3),
            'libreoffice_formula': f"=(h_gap + ({b3:.4f} * h_prev)) / {b1:.4f}",
            'latex_formula': f"P_{{flood}} = \\frac{{h_{{gap}} + ({b3:.4f} \\cdot h_{{prev}})}}{{{b1:.4f}}}",
        })
    print("Note: Threshold equations are reported for all fitted non-boundary clusters (C1-C4).\n")

    pd.DataFrame(table8_rows).to_csv(OUT_11_TABLE8_THRESHOLDS, index=False)

