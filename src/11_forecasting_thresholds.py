# ENTRY POINT
# ====================================================================================
def run_models() -> None:
    print("\nLoading input data...")
    df = load_data(MASTER_FILE)
    print(f"  [OK] {len(df)} monthly rows loaded | "
          f"Columns: {', '.join(df.columns.tolist())}")

    results_dict = run_state_space(df)
    run_peak_forecasting(df)
    run_summer_drought_forecasting(df)
    run_critical_flood_thresholds(results_dict)

    print("\n" + "=" * 70)
    print("  Analysis complete.")
    print("=" * 70 + "\n")


