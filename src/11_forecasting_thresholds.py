# SECTION 1: MECHANISTIC STATE-SPACE EQUATIONS (read from Script 03)
# ====================================================================================
def run_state_space(df: pd.DataFrame) -> dict:
    """
    Load cluster-level SSM coefficients from Script 03's mechanistic
    coefficients table (03_03_cluster_mechanistic_coefficients.csv).

    Script 03 fits the lag-1 SSM to each cluster centroid hydrograph:
        dh_t = b1*P_{t-1} - b2*PET_{t-1} - b3*h_{t-1}

    with rainfall and PET in metres (P_m, PET_m) and head in metres,
    so b1 and b2 are in m/m (dimensionless).  This function converts
    them to m/mm (/ 1000) for consistency with the downstream P_flood
    closed form (Section 3), which uses climatological P and PET in mm.

    Previously this function fitted its own lag-0 OLS on the cluster-
    average hydrographs from 03_regional_averages.csv. That independent
    fit produced weaker b1 values (lag-0 misses the 1-month recharge
    delay) and is superseded by the Script 03 lag-1 centroid fits.

    Returns:
        results_dict: {cluster_name: {beta1, beta2, beta3, r2, n}}
            b1, b2 in m/mm;  b3 dimensionless (positive).
    """
    print(sep("SECTION 1: MECHANISTIC STATE-SPACE EQUATIONS  (dh = b1*P - b2*PET - b3*h_prev)"))
    print("  Cluster-centroid lag-1 coefficients from Script 03")
    print(f"  Source: {OUT_03_MECHANISTIC_TABLE.name}\n")

    if not OUT_03_MECHANISTIC_TABLE.exists():
        sys.exit(
            f"\n[ERROR] Mechanistic coefficients file not found:\n"
            f"  {OUT_03_MECHANISTIC_TABLE}\n"
            f"  Run Script 03 (state-space model) first.\n"
        )

    cc = pd.read_csv(OUT_03_MECHANISTIC_TABLE)
    cluster_labels = CLUSTER_LABELS_STR
    results_dict = {}

    for _, row in cc.iterrows():
        cid = int(row["Cluster"])
        col = f"C{cid}"
        if col in EXCLUDED_CLUSTERS:
            continue

        # Script 03 stores beta in m/m (regression on P_m in metres).
        # Section 3 uses P_clim and PET_clim in mm, so convert to m/mm.
        b1 = float(row["beta_1"]) / 1000.0   # m/m -> m/mm
        b2 = float(row["beta_2"]) / 1000.0   # m/m -> m/mm
        b3_raw = float(row["beta_3"])
        if b3_raw < 0:
            print(f"  [WARNING] {col}: β₃ = {b3_raw:.4f} is negative "
                  f"(expected positive under displacement formulation). "
                  f"Using abs() but investigate upstream.")
        b3 = abs(b3_raw)                         # dimensionless, ensure positive

        r2 = float(row["R2"])
        n  = int(row["n"])

        pb1 = float(row["pvalue_beta_1"])
        pb2 = float(row["pvalue_beta_2"])
        pb3 = float(row["pvalue_beta_3"])

        label = cluster_labels.get(col, col)
        print(
            f"  {label}:  dh = ({b1:.6f}*P) - ({b2:.6f}*PET) - ({b3:.4f}*h_prev)"
            f"   |  R2 = {r2:.3f}   n = {n}"
        )
        print(
            f"        p-values:  P = {pb1:.4e}   PET = {pb2:.4e}   h_prev = {pb3:.4e}"
        )

        results_dict[col] = {
            'beta1': b1,
            'beta2': b2,
            'beta3': b3,
            'r2':    r2,
            'n':     n,
        }

    if EXCLUDED_CLUSTERS:
        print(f"\n  Excluded from mechanistic model: {', '.join(sorted(EXCLUDED_CLUSTERS))}")

    return results_dict



